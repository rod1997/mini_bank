public class TodosDados {

    public int id_conta;
    public int id_usuario;
    public int id_cliente;
    public String nome;
    public float saldo;

    public TodosDados(int id_conta,int id_usuario,int id_cliente,String nome,float saldo){

        this.id_conta = id_conta;
        this.id_usuario = id_usuario;
        this.id_cliente = id_cliente;
        this.nome = nome;
        this.saldo = saldo;

    }
}    